package datatype;

public class BasicDataType {
	public static void main(String args[]) {
		byte a = 126;
		a++;
		System.out.println(a);
		a++;
		System.out.println(a);
		System.out.println(2 + '\t' + 5);
		System.out.println(2 + "\t" + 5);
		int b = 0x1A75;
		int c = 0124;
		System.out.println(b);
		System.out.println(c);
	}
}
